# For Loop in Python

# Loop through a range of numbers
for i in range(5):
    print("Number:", i)

# Loop through a string
for char in "python":
    print("Character:", char)

# Loop through a list
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print("I like", fruit)

# Using range with start and step
for i in range(2, 11, 2):
    print("Even number:", i)