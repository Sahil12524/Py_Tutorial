a = 10
b = 3

# Arithmetic Operators
print("Addition: ", a + b)
print("Subtraction: ", a - b)
print("Multiplication: ", a * b)
print("Division: ", a / b)
print("Floor Division: ", a // b)
print("Modulus: ", a % b)
print("Power: ", a ** b)

# Comparison Operators
print()
print("a == b: ", a == b)
print("a != b: ", a != b)
print("a > b: ", a > b)
print("a < b: ", a < b)
print("a >= b: ", a >= b)
print("a <= b: ", a <= b)

# Logical Operators
x = True
y = False
print()
print("x and y: ", x and y)
print("x or y: ", x or y)
print("not x: ", not x)

# Membership Operators
name = "python"
print()
print("'py' in name: ", "py" in name)
print("'java' not in name: ", "java" not in name)

# Identity Operators
p = [1, 2, 3]
q = p
r = [1, 2, 3]
print()
print("p is q: ", p is q)
print("p is r: ", p is r)
print("p == r: ", p == r)